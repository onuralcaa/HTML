PRINTF KULLANIMI

• Tarayıcıya sadece metin çıkaran print ve echo işlevlerini zaten gördünüz.

• Ancak çok daha güçlü bir işlev olan printf, bir stringe özel biçimlendirme karakterleri koymanıza izin vererek çıktının formatını kontrol eder.

• Her formatlama karakteri için printf, bu formatı kullanarak göstereceği bir
argümanı iletmenizi bekler.

• Örneğin, aşağıdaki örnek, ondalık değerde 3 değerini görüntülemek için %d dönüşüm belirtecini kullanır:

• printf("There are %d items in your basket", 3);

• Eğer %d yerine %b yazılsaydı 11 görünürdü.