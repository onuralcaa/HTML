FOPEN ARGUEMENTS VALUES

• ‘r’ : sadece okuma (var olan dosyayı)
• ‘r+’ : okuma ve yazma (var olan dosyayı)
• ‘w’ : sadece yazma. (var olanı sıfırlar, olmayanı oluşturur)
• ‘w+’ : yukarıdakine okuma yetkisi de ekler.
• ‘a’ : sadece yazma. (var olan dosyanın sonuna yazar, olmayanı oluşturur)
• ‘a+’ : yukarıdakine okuma yetkisi de ekler.